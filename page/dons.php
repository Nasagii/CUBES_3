            <h2>Legs </h2> <h4>(Don par testament.)</h4>
            <br>Pour les legs notre association est reconnue d’intérêt général, et de ce fait exonérée de tous droits de succession en cas de legs lui revenant. 
                De nombreux adhérents nous ayant demandé la marche à suivre pour faire figurer la S.P.A. de Salon de Provence et sa région dans leurs dispositions testamentaires,
                c’est très volontiers que nous publions ci-après la procédure prévue à cet effet : 
            <br> « Ceci est mon testament : Lieu et date complète en toutes lettres.
            <br> Je soussigné(e) [nom, prénom, adresse] lègue à la Société Protectrice des Animaux de Salon de Provence et sa région ayant son siège Quartier du Talagard, 
                13300 Salon de Provence, la pleine propriété de mon bien ci-après désigné: (indiquer ici, soit l’immeuble ou les immeubles, soit les meubles ou espèces,
                soit la part d’actif net de la succession dont on désire faire bénéficier la S.P.A. de Salon de Provence et sa région. Indiquer si le legs est fait net de frais et droits ou non.) »
            <br> On peut ajouter un souhait quant à la destination du legs (ex : construction ou amélioration du refuge, installations vétérinaires, etc…). 
                Le testament peut être rédigé sur n’importe quel support papier mais doit être écrit de la propre main du testateur, qui datera et signera avec soin. 
                Quand on ne peut écrire, il faut avoir recours au testament reçu par acte notarié. Pour éviter une perte éventuelle, le testamentaire peut établir deux testaments semblables 
                (pas de photocopie, mais une seconde rédaction manuscrite) dont l’un sous enveloppe, daté et signé, est remis à une personne de confiance. 
                Pour tous renseignements complémentaires vous pouvez vous adresser à votre notaire ou à la Société Protectrice des Animaux de Salon de Provence et sa région Quartier du Talagard, 
                13300 Salon de Provence téléphone 04 90 42 20 77.
